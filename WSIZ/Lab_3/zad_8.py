tekst = "Python jest super"

print(f"Znak o indeksie zerowym: {tekst[0]}")
print(f"Znak o indeksie ostatnim: {tekst[-1]}")
print(f"Co drugi znak, zaczynając od zerowego: {tekst[::2]}")
print(f"Co trzeci znak, zaczynając od pierwszego: {tekst[1::3]}")
print(f"Od dziesiątego do ostatniego: {tekst[10:]}")
print(f"Łańcuch od końca do początku: {tekst[::-1]}")
